package com.example.course_project;

public class User {

    public String email ;
    public String fName ;
    public String lName ;
    public String password ;

    public User(){

    }

    public User(String email , String fName , String lName , String password){

        this.email = email;
        this.fName = fName;
        this.lName = lName;
        this.password = password;

    }

}
